package Com.tns.day3;

public class Loop {

	public static void main(String[] args) {
		// For loop//
		for (int i = 0; i < 5; i++) {
		    System.out.println("i = " + i);
		}                              
		
         //While loop//
		int k = 0;

		while (k < 5) {
		    System.out.println("k = " + k);
		    k++;
		}
		
		
        //Do-while//
		int j = 0;

		do {
		    System.out.println("j = " + j);
		    j++;
		} while (j < 5);


	}

}
